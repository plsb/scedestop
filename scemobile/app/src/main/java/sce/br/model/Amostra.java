public class Amostra {

    private int numAmostra;
    private String deposito;
    private int numLavas;

    public int getNumAmostra() {
        return numAmostra;
    }

    public void setNumAmostra(int numAmostra) {
        this.numAmostra = numAmostra;
    }

    public String getDeposito() {
        return deposito;
    }

    public void setDeposito(String deposito) {
        this.deposito = deposito;
    }

    public int getNumLavas() {
        return numLavas;
    }

    public void setNumLavas(int numLavas) {
        this.numLavas = numLavas;
    }
}
